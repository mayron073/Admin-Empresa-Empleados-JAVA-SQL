        // Metodo para borrar empleados de la db.
    public void removeEmployee(){
        String queryS = "SELECT idEmpleados FROM empleado WHERE documento = '"+doc+"'";
        try {
            connection = conexion.getConnection();
            st = connection.createStatement();
            rs = st.executeQuery(queryS);
            while(rs.next()){
                int idEmpleado = rs.getInt("idEmpleados");
                // Query "DELETE".
                String query = "DELETE FROM `empleado` WHERE idEmpleados = "+idEmpleado+"";
                // Crear coneccion con la db y ejecutar el query.
                try {
                    connection = conexion.getConnection();
                    st = connection.createStatement();
                    st.executeUpdate(query);
                //    JOptionPane.showMessageDialog(this,"Ha eliminado este empleado exitosomente", "", JOptionPane.INFORMATION_MESSAGE);    

                } catch(SQLException e) {
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el empleado", "",JOptionPane.WARNING_MESSAGE);
                }
            }
        }catch(SQLException e){
            System.out.println(e);
        }
        this.dispose();
    }
    
